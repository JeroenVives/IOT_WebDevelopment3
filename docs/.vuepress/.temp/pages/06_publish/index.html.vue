<h2 id="search-engine-optimization-seo" tabindex="-1"><a class="header-anchor" href="#search-engine-optimization-seo"><span>Search Engine Optimization (SEO)</span></a></h2>
<p>De kern van SEO is uitzoeken welke termen mensen waarschijnlijk zullen invoeren in een zoekmachine om uw site te vinden en dan deze termen te het gebruiken op de juiste plaatsen op uw site om de kans te vergroten dat zoekmachines een link naar uw site zullen tonen in hun resultaten.</p>
<p>Om te bepalen wie er eerste komt in de zoekresultaten, kijken zoekmachines niet alleen naar wat op uw site staat. Ze kijken ook hoeveel sites linken naar u (en hoe relevant die links zijn). Om deze reden wordt SEO vaak opgesplitst in twee gebieden: on-page technieken en off-page technieken.</p>
<p><strong>On-page technieken</strong></p>
<p>Dit zijn de methoden die je kan gebruiken op jouw webpagina's om hun 'rating' te verbeteren in zoekmachines.
Het belangrijkste onderdeel hiervan is het kijken naar trefwoorden die mensen waarschijnlijk zullen invoeren in een zoekmachine om jouw site te vinden, en dan deze dan in de tekst en HTML-code voor jouw site op te nemen om de zoekmachines te helpen ontdekken dat jouw site deze onderwerpen behandelt.</p>
<p>Zoekmachines vertrouwen zeer sterk op de tekst die in jouw pagina's staat, dus het is belangrijk dat de juiste zoektermen in de tekst staan. Er zijn zeven essentiële plaatsen hiervoor waar dit kan.</p>
<p><strong>Off-page technieken</strong></p>
<p>Andere sites naar jouw site laten linken is net zo belangrijk als op de on-page technieken. Zoekmachines bepalen de belangrijkheid van jouw pagina door te bekijken hoeveel andere websites naar jouw pagina linken. Ze zijn bijzonder geïnteresseerd naar sites waarvan de inhoud gerelateerd is naar de jouwe. Zoekmachines kijken ook naar de woorden tussen de opening <code v-pre>&lt;a&gt;</code> tag en afsluitende <code v-pre>&lt;/a&gt;</code> tag in de koppeling. Als de tekst in de link informatie bevat i.p.v. 'klik hier' is dat meer relevant.</p>
